        <strong><h1>1.Progressive Web Apps</h1></strong>
    </div>
    <div class="text1">
        <p>
            Es una solución basada en webs tradicionales pero incorporando
            algunas particularidades. Estas particularidades la hacen 
            parecerse a una app nativa para teléfonos móviles y tablets.
            Todos los proyectos son independientes y tienen diversos 
            niveles de adaptación de la página web. Cualquier usuario 
            puede instalarla en la pantalla de inicio de su dispositivo.
            Este año hemos visto un avance en el desarrollo de estas
            aplicaciones con nuevos lenguajes, técnicas de programación 
            reactiva de las Progressive Web Apps y frameworks. Aportan muchas
            facilidades a los usuarios y tienen muy pocos inconvenientes.
        </p>
