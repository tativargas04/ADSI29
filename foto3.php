    </div>
    <div class="text1">
        <p>
            Es una nueva tecnología que permite ejecutar aplicaciones 
            en nuestro móvil o tablet sin necesidad de instalarlas. 
            Además, no se gasta apenas recursos ni almacenamiento interno. 
            te tipo de aplicaciones se pueden abrir a través de enlaces web,
             con ello el usuario tiene la posibilidad de visualizar dichas 
             apps antes de descargarlas, con el objetivo de hacerse una idea 
             de si merece la pena instalarlas o no. Cada vez son más las 
             empresas que utilizan esta tecnología como BuzzFeed o Skyscanner.
        </p>
